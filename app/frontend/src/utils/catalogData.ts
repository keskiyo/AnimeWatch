export function filterGenreOptions(query: string): string[] {
	const normalizedQuery = query.trim().toLowerCase()

	if (!normalizedQuery) {
		return [...GENRE_OPTIONS]
	}

	return GENRE_OPTIONS.filter(genre =>
		genre.toLowerCase().includes(normalizedQuery),
	)
}
